// base 프레임을 깊은 복사 후 지정 셀만 덮어쓴다. [row, col, value] 트리플.
function applyDeltas(base, deltas) {
  const f = base.map(row => [...row]);
  for (const [r, c, v] of deltas) f[r][c] = v;
  return f;
}

